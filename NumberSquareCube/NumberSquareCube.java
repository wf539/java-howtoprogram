// Draw a box, an oval, an arrow and a diamond with asterisks.

public class NumberSquareCube
{
   // main method begins execution of Java application
   public static void main( String args[] )
   {
      
      System.out.println( "number  square  cube" );
      System.out.println( 0 + "       " + 0 * 0 + "       " + 0 * 0 * 0 );
      System.out.println( 1 + "       " + 1 * 1 + "       " + 1 * 1 * 1 );
      System.out.println( 2 + "       " + 2 * 2 + "       " + 2 * 2 * 2 );
      System.out.println( 3 + "       " + 3 * 3 + "       " + 3 * 3 * 3 );
      System.out.println( 4 + "       " + 4 * 4 + "      " + 4 * 4 * 4 );
      System.out.println( 5 + "       " + 5 * 5 + "      " + 5 * 5 * 5 );
      System.out.println( 6 + "       " + 6 * 6 + "      " + 6 * 6 * 6 );
	  System.out.println( 7 + "       " + 7 * 7 + "      " + 7 * 7 * 7 );
      System.out.println( 8 + "       " + 8 * 8 + "      " + 8 * 8 * 8 );
      System.out.println( 9 + "       " + 9 * 9 + "      " + 9 * 9 * 9 );
      System.out.println( 10 + "      " + 10 * 10 + "     " + 10 * 10 * 10 );
      
   } // end method main

} // end class NumberSquareCube

